print("\n *** Convert Decimal Number into Binary, Octal, HexaDecimal *** \n")
number = int(input(" Enter a Number : "))

print("\n The Binary value for {} is {}".format(number, bin(number)))
print("\n The octal value for {} is {}".format(number, oct(number)))
print("\n The Hexadeciaml value for {} is {}".format(number, hex(number)))
